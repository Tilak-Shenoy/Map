public class AlertDialogActivity extends AppCompatActivity {

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_alert_dialog);
        ListView listView=findViewById(R.id.routes);
        TextView textView=findViewById(R.id.textView);
        Bundle bundle=getIntent().getExtras();
        ArrayList route=bundle.getCharSequenceArrayList("Route");
        if (route==null || route.isEmpty()){
            textView.setVisibility(View.VISIBLE);
            listView.setVisibility(View.GONE);
            return;
        }
        Log.d("Route", String.valueOf(route));
        ArrayAdapter adapter=new ArrayAdapter(this, android.R.layout.simple_list_item_1,route);
        listView.setAdapter(adapter);
    }
}
